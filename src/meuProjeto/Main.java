package meuProjeto;

public class Main {
    public static void main(String[] args){
        int a = 10;
        int b = 2;
        double r = 0.0;

        r = a + b;
        System.out.println("soma: "+r);
        
        r = a - b;
        System.out.println("subtração: "+r);

        r = a * b;
        System.out.println("multiplicação: "+r);
        
        r = a / b;
        System.out.println("divisão: "+r);

        r = a % b;
        System.out.println("reto: "+r);
    }
}
